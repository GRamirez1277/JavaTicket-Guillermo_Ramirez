    private void crearEventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearEventoActionPerformed
        double montoEv;
        int cantidad;
        if (!tituloEvento.getText().isBlank() && !descripcionEvento.getText().isBlank() && !montoEvento.getText().isBlank() && !cantidadPersonas.getText().isBlank()) {
            if (calendarioEvento.getDate() != null) {
                try {
                    montoEv = Double.parseDouble(montoEvento.getText());
                } catch (NumberFormatException er) {
                    JOptionPane.showMessageDialog(null, "El monto debe ser un número");
                    return;
                }

                try {
                    cantidad = Integer.parseInt(cantidadPersonas.getText());
                } catch (NumberFormatException er) {
                    JOptionPane.showMessageDialog(null, "La cantidad de personas tiene que ser un número entero");
                    return;
                }

                if (cantidad >= 0 && montoEv >= 0) {
                    if (cantidad <= 20000) {
                        if (seleccionado != null) {

                            Eventos eve = new EventoMusical(ID, tituloEvento.getText(), descripcionEvento.getText(), calendarioEvento.getDate(), montoEv, cantidad, seleccionado, 0, 1, new ArrayList(), false);
                            Login.MANEJOEVENTOS.AddEvent(eve, ID);
                            JOptionPane.showMessageDialog(null, "Se ha creado el evento");
                            MenuEventos me = new MenuEventos();
                            me.setVisible(true);
                            calendarioEvento = null;

                            if (Login.FUNCIONES.buscar(ManejoUsuarios.USERACTUAL).getTipoUsuario() == 1) {
                                Administrador adm = (Administrador) Login.FUNCIONES.buscar(ManejoUsuarios.USERACTUAL);
                                adm.AgregarID(ID);
                            }
                            if (Login.FUNCIONES.buscar(ManejoUsuarios.USERACTUAL).getTipoUsuario() == 2) {
                                Contenidos cts = (Contenidos) Login.FUNCIONES.buscar(ManejoUsuarios.USERACTUAL);
                                cts.AgregarID(ID);
                            }
                            dispose();
                        } else {
                            JOptionPane.showMessageDialog(null, "No ha seleccionado ningún tipo de deporte");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "No pueden haber más de 20,000 personas");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Tiene que ser una cantidad positiva");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Necesita seleccionar una fecha");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");
        }
    }//GEN-LAST:event_crearEventoActionPerformed

