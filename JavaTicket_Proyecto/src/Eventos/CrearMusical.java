    private void tipoMusicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tipoMusicaActionPerformed
        String tipoMusicaSeleccionada = (String) tipoMusica.getSelectedItem();
    switch (tipoMusicaSeleccionada) {
        case "POP":
            seleccionado = TipoMusica.POP;
            break;
        case "ROCK":
            seleccionado = TipoMusica.ROCK;
            break;
        case "RAP":
            seleccionado = TipoMusica.RAP;
            break;
        case "CLASICA":
            seleccionado = TipoMusica.CLASICA;
            break;
        case "REGGEATON":
            seleccionado = TipoMusica.REGGEATON;
            break;
        case "OTRO":
            seleccionado = TipoMusica.OTRO;
            break;
        default:
            seleccionado = null;
            break;
    }
    }//GEN-LAST:event_tipoMusicaActionPerformed

    int ID = obtenerNuevoId();
    private int obtenerNuevoId() {
        int nuevoId = 0;
        while (Login.MANEJOEVENTOS.buscar(nuevoId) != null) {
            nuevoId++;
        }
        return nuevoId;
    }

